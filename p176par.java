public class p76par {  
		public static void main(String args[]){  
		int a[]={7, 2, 4, 1, 3, 5, 6, 8, 2, 10};  
		System.out.println("Even Numbers:");  
		for(int i=0;i<a.length;i++){  
		if(a[i]%2!=0){  
		System.out.println(a[i]);  
		}  
		}  
		System.out.println("odd Numbers:");  
		for(int i=0;i<a.length;i++){  
		if(a[i]%2==0){  
		System.out.println(a[i]);  
		}  
		}  
		}}  
